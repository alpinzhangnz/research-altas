export function searchArticles(all, query) {
  const q = query.trim().toLowerCase();
  if (!q) return all.slice(0, 15);
  const scored = all.map((a) => {
    let score = 0;
    if (a.title.toLowerCase().includes(q)) score += 6;
    if (a.summary.toLowerCase().includes(q)) score += 4;
    if (a.content.toLowerCase().includes(q)) score += 2;
    for (const t of a.tags) if (t.toLowerCase().includes(q)) score += 3;
    return { a, score };
  });
  return scored
    .filter((x) => x.score > 0)
    .sort((x, y) => y.score - x.score)
    .map((x) => x.a)
    .slice(0, 50);
}

export function topTags(all) {
  const counts = new Map();
  for (const a of all) {
    for (const t of a.tags) counts.set(t, (counts.get(t) || 0) + 1);
  }
  return Array.from(counts.entries())
    .sort((a, b) => b[1] - a[1])
    .slice(0, 12)
    .map(([name]) => name);
}